static struct dm_register_ipcore_2_s core2_dm_regs;
static struct dm_lut_ipcore_s core2_lut;
int dolby_core2_meta(
	uint32_t *dm_num,
	uint32_t *lut_num,
	uint32_t *p_core2_dm_regs,
	uint32_t *p_core2_lut)
{
	uint32_t data32 = 0;
	uint32_t dm_count = 0;
	uint32_t lut_count = 0;
	int i;
	int ret;

	/* core2 metadata registers */
	ret = metadata_read_u32(&dm_count);
	if (ret < 0) {
		pr_dolby_error("no dm core2...\n");
		return -1;
	}
	for (i = 0; i < dm_count; i++) {
		ret = metadata_read_u32(&data32);
		if (ret < 0) {
			pr_dolby_error("no enough dm core2[%d]...\n", i);
			return -1;
		}
		p_core2_dm_regs[i] = data32;
	}
	*dm_num = dm_count;

	/* core2 lookup table */
	ret = metadata_read_u32(&lut_count);
	if (ret < 0) {
		pr_dolby_error("no lut core2...\n");
		return -1;
	}
	for (i = 0; i < lut_count; i++) {
		ret = metadata_read_u32(&data32);
		if (ret < 0) {
			pr_dolby_error("no enough lut core2[%d]...\n", i);
			return -1;
		}
		p_core2_lut[i] = data32;
	}
	*lut_num = lut_count;
	return 0;
}

int dolby_core2_set(
	uint32_t dm_count,
	uint32_t lut_count,
	uint32_t *p_core2_dm_regs,
	uint32_t *p_core2_lut,
	int hsize,
	int vsize,
	int dolby_enable)
{
	uint32_t count;
	int i;

	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL0, 0);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL1,
		((hsize + g_htotal_add) << 16)
		| (vsize + g_vtotal_add + g_vsize_add));
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL3, (g_hwidth << 16) | g_vwidth);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL4, (g_hpotch << 16) | g_vpotch);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL2,
		(hsize << 16) | (vsize + g_vsize_add));
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL5, 0x0);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_DMA_CTRL, 0x0);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_REG_START + 2, 1);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_REG_START + 1, 2);
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_REG_START + 1, 2);

	if ((dolby_vision_flags & FLAG_RESET_EACH_FRAME) && (dm_count == 0))
		count = 24;
	else
		count = dm_count;
	for (i = 0; i < count; i++)
		VSYNC_WR_MPEG_REG(DOLBY_CORE2A_REG_START + 6 + i,
			p_core2_dm_regs[i]);
	/* core2 metadata program done */
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_REG_START + 3, 1);

	if (dolby_vision_flags & FLAG_RESET_EACH_FRAME)
		count = 256 * 5;
	else
		count = lut_count;
	if (count) {
		if (dolby_vision_flags & FLAG_CLKGATE_WHEN_LOAD_LUT)
			VSYNC_WR_MPEG_REG_BITS(DOLBY_CORE2A_CLKGATE_CTRL,
				2, 2, 2);
		VSYNC_WR_MPEG_REG(DOLBY_CORE2A_DMA_CTRL, 0x1401);
		for (i = 0; i < count; i++)
			VSYNC_WR_MPEG_REG(DOLBY_CORE2A_DMA_PORT,
				p_core2_lut[i]);
		/* core1 lookup table program done */
		if (dolby_vision_flags & FLAG_CLKGATE_WHEN_LOAD_LUT)
			VSYNC_WR_MPEG_REG_BITS(
				DOLBY_CORE2A_CLKGATE_CTRL, 0, 2, 2);
	}

	/* enable core2 */
	VSYNC_WR_MPEG_REG(DOLBY_CORE2A_SWAP_CTRL0, dolby_enable << 0);
	return 0;
}

